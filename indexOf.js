//  indexOf()  =>  it return the the first match item index in the start occurrence  
//                 array. and if item is not avalilable in the array it return -1. 

const countries = ["India", "America", "Poland", "Norway", "Scotland", "Argentina", "Brazil", "Mexico", "Caneda"]

const indexOfPoland = countries.indexOf("Poland")
console.log(indexOfPoland)


//  lastIndexOf()  => it works as same but find the index of last occurence.

const lastIndexEle = countries.lastIndexOf("Mexico")
console.log(lastIndexEle)
